'''Faça um Programa que calcule a área de um quadrado,
em seguida mostre o dobro desta área para o usuário. A=B*H'''

base = int(input("Digite o valor da base do quadrado: "))
altura = int(input("Digite o valor da altura do quadrado: "))

area = base*altura

print(f"O valor da área do quadrado é {area} e seu dobro é {area * 2}")









